             # STRING FORMATTING

num1 = 3.123455
num2 = 45.4444

# FORMAT METHOD

print("num one is {0:.3f} and num two {1:.3f} is".format(num1, num2))

# add a colon and a dot {}:.} to get the precision of the number you want
# add {f} to get the number of floats

# USING F-STRING
print(f"num one is {num1:.3f} and num two is {num2:.3f}")
